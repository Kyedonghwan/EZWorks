CREATE TABLE EMP (
   EMP_NO NUMBER NOT NULL, /* 荤盔锅龋 */
   EMP_NAME VARCHAR2(50) NOT NULL, /* 荤盔捞抚 */
   EMP_ENAME VARCHAR2(50) NOT NULL, /* 荤盔康绢捞抚 */
   EMP_PWD VARCHAR2(100) NOT NULL, /* 厚剐锅龋 */
   EMP_TEL VARCHAR2(50) NOT NULL, /* 傈拳锅龋 */
   EMP_SSN VARCHAR2(50) NOT NULL, /* 林刮锅龋 */
   EMP_EMAIL VARCHAR2(100) NOT NULL, /* 捞皋老 */
   EMP_ZIPCODE VARCHAR2(50) NOT NULL, /* 快祈锅龋 */
   EMP_ADDR VARCHAR2(200) NOT NULL, /* 林家 */
   EMP_ADDR2 VARCHAR2(200) NOT NULL, /* 惑技林家 */
   EMP_HIREDATE TIMESTAMP NOT NULL, /* 涝荤老 */
   EMP_OUTDATE TIMESTAMP, /* 硼荤老 */
   EMP_IMG VARCHAR2(100), /* 荤盔荤柳 */
   DEPT_NO NUMBER NOT NULL, /* 何辑内靛 */
   POS_NO NUMBER NOT NULL /* 流鞭锅龋 */
);

CREATE UNIQUE INDEX PK_EMP
   ON EMP (
      EMP_NO ASC
   );

ALTER TABLE EMP
   ADD
      CONSTRAINT PK_EMP
      PRIMARY KEY (
         EMP_NO
      );

alter table emp
add constraint fk_emp_dept
foreign key(dept_no)
references dept(dept_no);

alter table emp
add constraint fk_emp_position
foreign key(pos_no)
references position(pos_no);

